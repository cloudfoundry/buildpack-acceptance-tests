package integration_test

import (
	"path/filepath"

	"github.com/cloudfoundry/libbuildpack/cutlass"

	. "github.com/onsi/ginkgo"
	. "github.com/onsi/gomega"
)

var _ = Describe("CF NodeJS Buildpack", func() {
	var app *cutlass.App
	AfterEach(func() {
		if app != nil {
			app.Destroy()
		}
		app = nil
	})

	Context("deploying a Node.js app with empty node_modules directory", func() {
		BeforeEach(func() {
			app = cutlass.New(filepath.Join(testdata, "empty_node_modules"))
		})

		It("outputs protip that recommends user vendors dependencies", func() {
			PushAppAndConfirm(app)
			Expect(app.Stdout.String()).To(MatchRegexp("It is recommended to vendor the application's Node.js dependencies"))
		})
	})
})
